package it.polimi.se2019.model;

import java.io.Serializable;

public class Coordinates implements Serializable {

    private final int x;
    private final int y;

    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
